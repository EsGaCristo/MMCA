    public void actualizar(){        
        for (int i = 0; i >=dtm.getRowCount()-1 ; i++) {
            dtm.removeRow(i);
        }
        dtm.setRowCount(0);
    categorias.clear();
    buscar();
    llenarTabla();
    }
    public void agregar(){
        Categorias user = new Categorias(txtCat.getText());
        user.insertar();
        actualizar();
        txtCat.setText("");
    }

     private void buscar() {         
        ResultSet resultado=null;
        Connection connection=null;
        Statement statement=null;
        try {
            connection = bd.getConexion();
            statement = connection.createStatement();
            
            String selectSql = "SELECT * from TIPOSSERVICIO";
            resultado= statement.executeQuery(selectSql);
          
            while(resultado.next()){
            Categorias c = new Categorias(resultado.getString("NOMBRE"));
            categorias.add(c);
            }
           
        } catch (Exception ex) {
            ex.printStackTrace();
            
        }finally{
            bd.cerrar(statement, resultado);
            llenarTabla();
        }
    }
     public void llenarTabla(){
         Object O[]=null;
         for (int i = 0; i < categorias.size(); i++) {
            dtm.addRow(O);
            Categorias cat = (Categorias) categorias.get(i);  
           dtm.setValueAt(cat.getNombre(), i, 0);
            }
     }
