    public void agregar(){
        int rango;
        if (cmbPermisos.getSelectedItem().equals("Administrador")) {
        rango = 1;  
        }else if(cmbPermisos.getSelectedItem().equals("Cotizaciones")){
        rango = 2;  
        }else{rango = 3;}
        Personal user = new Personal(txtNombre.getText(),txtPass.getText(),rango);
        user.insertar();
    }

